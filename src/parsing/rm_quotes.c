#include "../includes/minishell.h"
#include "../includes/parser.h"

char	*remove_quotes(const char *str)
{
	char	*result;
	int		i;
	int		j;
	char	quote;

	i = 0;
	j = 0;
	result = gc_alloc(ft_strlen(str) + 1);
	while (str[i])
	{
		if (str[i] == SINGLE_QUOTES || str[i] == DOUBLE_QUOTES)
		{
			quote = str[i++];
			while (str[i] && str[i] != quote)
				result[j++] = str[i++];
			if (str[i] == quote)
				i++;
		}
		else
			result[j++] = str[i++];
	}
	result[j] = '\0';
	return (result);
}
