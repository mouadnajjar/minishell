int	ft_isspace(char c)
{
	return (c == ' ' || c == '\t' || c == '\n'
		|| c == '\v' || c == '\f' || c == '\r');
}

int	is_special(const char *s)
{
	return (*s == '|' || *s == '<' || *s == '>');
}
